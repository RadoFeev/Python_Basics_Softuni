fruit = input()
working_day = input()
quantity = float(input())
price = 0
if working_day == "Saturday" or working_day == "Sunday":
    if fruit == "banana":
        price = 2.70
    elif fruit == "apple":
        price = 1.25
    elif fruit == "orange":
        price = 0.90
    elif fruit == "grapefruit":
        price = 1.60
    elif fruit == "kiwi":
        price = 3.00
    elif fruit == "pineapple":
        price = 5.60
    elif fruit == "grapes":
        price = 4.20
elif working_day == "Monday" or working_day == "Tuesday" or working_day == "Wednesday" or working_day == "Thursday" or working_day == "Friday":
    if fruit == "banana":
        price = 2.50
    elif fruit == "apple":
        price = 1.20
    elif fruit == "orange":
        price = 0.85
    elif fruit == "grapefruit":
        price = 1.45
    elif fruit == "kiwi":
        price = 2.70
    elif fruit == "pineapple":
        price = 5.50
    elif fruit == "grapes":
        price = 3.85
if price == 0:
    print("error")
else:
    sum_price = quantity * price
    print(f"{sum_price:.2f}")